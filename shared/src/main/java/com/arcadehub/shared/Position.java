package com.arcadehub.shared;

import java.io.Serializable;

public class Position implements Serializable {
    private float x;
    private float y;

    public Position(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
